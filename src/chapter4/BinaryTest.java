package chapter4;

public class BinaryTest {
    public static void main(String[] args){
        // n1 二进制 0b开头
        int n1 = 0b1010;
        // n2 10进制
        int n2 = 1010;
        // n3 8进制，0开头
        int n3 = 01010;
        // n4 16进制 0x开头
        int n4 = 0x1010;
        System.out.println(n1);
        System.out.println(n2);
        System.out.println(n3);
        System.out.println(n4);
    }
}
