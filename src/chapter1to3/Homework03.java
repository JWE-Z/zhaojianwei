package chapter1to3;

public class Homework03 {
    public static void main(String[] args){
        // + 加号拼接两个string
        String book1 = "天龙八部";
        String bookk2 = "笑傲江湖";
        System.out.println(book1 + bookk2);

        //+ 加号拼接两个char
        // m.out.性别用char保存
        char nan = '男';
        char nv = '女';
        System.out.println(nan + nv);//得到‘男’的码值 加上 ‘女’的码值，是一个整数

        // 保存两本书的价格
        double price1 = 123.56;
        double price2 = 100.22;
        System.out.println(price1 + price2);// 数值相加
    }
}
