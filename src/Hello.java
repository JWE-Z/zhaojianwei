public class Hello {  //hello 是一个public共有的类，{表示这个类的开始

    // 写一个mian方法
    public static void main(String[] args){  // 表示一个主方法，我们程序的入口,有固定的书写格式
        System.out.println("hello world~");   // 输出，分号；表示语句结束
    }
} // hello类的结束

